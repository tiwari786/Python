# Tuple (tuple)
# Same as list but cannot be changed.

# - Ordered
# - Not changeable
# - Faster & safer

# Create tuple
roles = ("admin", "user", "guest")

# Access tuple items
print(roles[2])  # guest


# Why tuple?
# Used for fixed data like constants

# example:
HTTP_STATUS = (200, 201, 400, 404)
